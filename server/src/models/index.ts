import { Sequelize } from "sequelize";
import { UserFileFactory } from "./userFile";

const sequelize = process.env.DB_URL
  ? new Sequelize(process.env.DB_URL)
  : new Sequelize(
      process.env.DB_NAME || "",
      process.env.DB_USER || "",
      process.env.DB_PASSWORD,
      {
        host: "localhost",
        dialect: "postgres",
        dialectOptions: {
          decimalNumbers: true,
        },
      }
    );

const UserFile = UserFileFactory(sequelize);

export { sequelize, UserFile };
